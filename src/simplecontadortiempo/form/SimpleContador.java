        public void actualizarContador(String tipo) {

        ActionListener accionActualizar = new ActionListener() {

            @Override

            public void actionPerformed(ActionEvent event) {

                contador.contarSegundos(labelSegundos, labelMinutos);
                String tituloDinamico = labelMinutos.getText() + " : "  + labelSegundos.getText();
                actualizarTitulo(tituloDinamico);
                

            }

        };


        if(tipo.equals("star"))
        {
        
            if(temporizador == null ){
            temporizador = new Timer(1000, accionActualizar);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Temporizador ya iniciado.");
            }
        
        }
        
        switch(tipo)
        {
            case "star"     : 
                temporizador.start();
            break;
            
            case "stop"     : 
                
                if(temporizador.isRunning()){
                temporizador.stop();
                }
                else
                {
                temporizador.start();
                }
                
            break;
            
            case "reset"   : 
                // temporizador.restart();
                contador.reiniciar(labelSegundos, labelMinutos);
                temporizador.stop();
                temporizador = null;
            break;
        }
        

    }
    

        
        
